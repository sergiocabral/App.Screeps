/**
 * Nível de log.
 */
export enum LogLevel {
  /**
   * Informações detalahadas sobre o funcionamento do sistema.
   */
  Verbose = 0,

  /**
   * Informações de depuração.
   */
  Debug = 1,

  /**
   * Informações em geral.
   */
  Information = 2,

  /**
   * Pontos de atenção.
   */
  Warning = 3,

  /**
   * Erros de sistema.
   */
  Error = 4
}
